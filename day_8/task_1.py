# define a function to print
def greet():
    print("Hello Jorge")
    print("How are you?")
    print("It's been so long we've met!")

greet()

# define another function with a parameter
def greet_with_name(name):
    print(f"Hello {name}")
    print(f"How are you?")
    print(f"It's been so long we've met!")

greet_with_name("Nikhil")